public class Increment {
    public static void main(String[] args) {
        int a =10;
        int b= ++a; // Here we are doing pre increment 
        System.out.println(b);

        int c= 23;
        int d =c++; //Here we are doing Post Increment. First it will assing value then increment

        System.out.println(d);
        System.out.println(c);
        // Same thing -- operator will.
    }
    
}
